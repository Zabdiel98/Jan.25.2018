
public class Laptop {
	String name;
	String system;
	int year;
	
public Laptop(String name2, String system2, int year2) {
	this.name=name2;
	this.system=system2;
	this.year=year2;
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

public String getSystem() {
	return system;
}

public void setSystem(String system) {
	this.system = system;
}

public int getYear() {
	return year;
}

public void setYear(int year) {
	this.year = year;
}

}
